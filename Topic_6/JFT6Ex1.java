//A fisherman will be allowed to go on a fishing trip, if he is in possession of a fishing permit.
//Create a Java program which creates a boolean variable named hasPermit and assign it a value of true.
//Use an if-else statement to test the value of the variable and output a suitable message to the console.

public class JFT6Ex1 {

    public static void main(String[] args) {

        boolean hasPermit = false;

        if(hasPermit == true)

            {
                System.out.println("Enjoy your fishing trip!!");
            }
        else

            {
                System.out.println("Sorry, no fishing for you today :'( ");
            }

    }
}